// HTTP Handlers (exported so they can be mounted on an external mux)
// ---------------------------------------------------------------------------

// HandleHealth returns a JSON health payload with uptime and alert count.
func (tb *TwinBridge) HandleHealth(w http.ResponseWriter, _ *http.Request) {
	tb.mu.RLock()
	uptime := time.Since(tb.startAt).Round(time.Second).String()
	tb.mu.RUnlock()

	w.Header().Set("Content-Type", "application/json")
	json.NewEncoder(w).Encode(map[string]interface{}{
		"status": "ok",
		"uptime": uptime,
		"alerts": tb.store.Len(),
	})
}

// HandlePair generates a new session token, shows a macOS approval dialog,
// and returns the token on approval. The token is also persisted to the
// discovery file.
func (tb *TwinBridge) HandlePair(w http.ResponseWriter, r *http.Request) {
	if !isLocalhost(r.RemoteAddr) {
		http.Error(w, "forbidden", http.StatusForbidden)
		return
	}

	tok, err := generateToken()
	if err != nil {
		http.Error(w, "failed to generate token", http.StatusInternalServerError)
		return
	}

	// Show macOS approval dialog via osascript. On non-macOS systems (or if
	// osascript is unavailable) the dialog is skipped and pairing proceeds
	// automatically — useful for development / testing.
	approved := showPairingDialog()
	if !approved {
		slog.Info("twin bridge: pairing rejected by user")
		http.Error(w, "pairing rejected", http.StatusForbidden)
		return
	}

	tb.mu.Lock()
	tb.token = tok
	port := tb.activePort
	tb.mu.Unlock()

	if port == 0 {
		port = tb.cfg.Port
	}
	if err := writeDiscoveryFile(port, tok); err != nil {
		slog.Warn("twin bridge: pair — failed to update discovery file", "error", err)
	}

	slog.Info("twin bridge: paired", "remote", r.RemoteAddr)

	w.Header().Set("Content-Type", "application/json")
	json.NewEncoder(w).Encode(map[string]string{
		"token": tok,
	})
}

// HandleWS upgrades an HTTP connection to WebSocket and starts the event loop.
func (tb *TwinBridge) HandleWS(w http.ResponseWriter, r *http.Request) {
	conn, err := upgrader.Upgrade(w, r, nil)
	if err != nil {
		slog.Warn("twin bridge: ws upgrade failed", "error", err, "remote", r.RemoteAddr)
		return
	}
	defer conn.Close()

	slog.Info("twin bridge: ws client connected", "remote", r.RemoteAddr)
	tb.handleConnection(conn)
	slog.Info("twin bridge: ws client disconnected", "remote", r.RemoteAddr)
}

// handleConnection authenticates a WebSocket connection and then processes
// events until the connection is closed.
func (tb *TwinBridge) handleConnection(conn *websocket.Conn) {
	conn.SetReadLimit(maxMessageBytes)

	// --- Auth phase ---
	if err := conn.SetReadDeadline(time.Now().Add(authTimeout)); err != nil {
		slog.Warn("twin bridge: set auth deadline failed", "error", err)
		return
	}

	_, raw, err := conn.ReadMessage()
	if err != nil {
		slog.Warn("twin bridge: auth read failed", "error", err)
		return
	}

	var msg authMessage
	if err := json.Unmarshal(raw, &msg); err != nil || msg.Type != "auth" {
		slog.Warn("twin bridge: invalid auth message")
		writeWSError(conn, "expected auth message")
		return
	}

	tb.mu.RLock()
	validToken := tb.token
	tb.mu.RUnlock()

	if validToken == "" || msg.Token != validToken {
		slog.Warn("twin bridge: auth token mismatch", "extension_id", msg.ExtensionID)
		writeWSError(conn, "invalid token")
		return
	}

	if msg.ExtensionID == "" {
		slog.Warn("twin bridge: missing extension_id")
		writeWSError(conn, "missing extension_id")
		return
	}

	// Auth succeeded — clear read deadline for normal operation.
	if err := conn.SetReadDeadline(time.Time{}); err != nil {
		slog.Warn("twin bridge: clear deadline failed", "error", err)
		return
	}

	slog.Info("twin bridge: extension authenticated", "extension_id", msg.ExtensionID)

	// Confirm auth success.
	writeWSJSON(conn, map[string]string{"type": "auth_ok"})

	// Register this connection as the active command target so MCP browser
	// tools can send bidirectional commands to the extension.
	cs := newCommandState(conn)
	setActiveState(cs)
	defer clearActiveState()

	// --- Event loop ---
	for {
		if err := conn.SetReadDeadline(time.Now().Add(readTimeout)); err != nil {
			slog.Warn("twin bridge: set read deadline failed", "error", err)
			return
		}

		_, raw, err := conn.ReadMessage()
		if err != nil {
			if websocket.IsUnexpectedCloseError(err,
				websocket.CloseGoingAway,
				websocket.CloseNormalClosure,
				websocket.CloseNoStatusReceived,
			) {
				slog.Warn("twin bridge: unexpected ws close", "error", err)
			}
			return
		}

		// Peek at the message type to route it correctly.
		var peek struct {
			Type string `json:"type"`
		}
		if err := json.Unmarshal(raw, &peek); err != nil {
			slog.Warn("twin bridge: malformed message", "error", err)
			continue
		}

		// Route command responses back to waiting callers.
		if peek.Type == "response" {
			var resp struct {
				ID     string          `json:"id"`
				Result json.RawMessage `json:"result"`
			}
			if err := json.Unmarshal(raw, &resp); err != nil {
				slog.Warn("twin bridge: malformed response", "error", err)
				continue
			}
			cs.handleCommandResponse(resp.ID, resp.Result)
			continue
		}

		// All other messages are treated as BridgeEvents (alerts).
		var event BridgeEvent
		if err := json.Unmarshal(raw, &event); err != nil {
			slog.Warn("twin bridge: malformed event", "error", err)
			continue
		}

		// Route meeting lifecycle events to the meeting session manager.
		// Captions stay local — they do NOT go into the alert store.
		switch event.Type {
		case "meeting_started":
			tb.HandleMeetingStarted(event)
			slog.Debug("twin bridge: meeting started", "source", event.Source)
			continue
		case "caption":
			tb.HandleCaption(event)
			// Captions are not stored as alerts (privacy-sensitive)
			continue
		case "meeting_ended":
			tb.HandleMeetingEnded(event)
			slog.Debug("twin bridge: meeting ended", "source", event.Source)
			// HandleMeetingEnded creates the meeting_summary alert — skip generic alert creation
			continue
		case "meeting_detected":
			// This is informational only (opt-in prompt was shown by service worker)
			slog.Debug("twin bridge: meeting detected", "source", event.Source)
			// Fall through to create a regular alert so the user can see it in twin_alerts
		case "twin_log":
			// Monitor data pushed from the extension — store in local alert store
			// and index in the local RAG system for the twin agent
			slog.Info("twin bridge: twin_log received", "source", event.Source)
			// Fall through to store in alert store
		}

		alert := Alert{
			ID:        uuid.New().String(),
			Source:    event.Source,
			Type:      event.Type,
			Data:      event.Data,
			CreatedAt: time.Now().UTC(),
			Read:      false,
		}
		tb.store.Add(alert)

		slog.Debug("twin bridge: event stored",
			"id", alert.ID,
			"source", alert.Source,
			"type", alert.Type,
		)

		// Trigger macOS notification for high-priority events.
		if shouldNotify(event) {
			title := fmt.Sprintf("Orchestra — %s", strings.Title(event.Source))
			body := fmt.Sprintf("New %s event", event.Type)
			go sendDesktopNotification(title, body)
		}
	}
}

