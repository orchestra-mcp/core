// Package twin implements the Twin Bridge WebSocket server. It receives events
// from a paired Chrome extension and aggregates them into Alerts that the
// digital twin (and MCP tools) can query.
//
// Architecture overview:
//
//	Chrome extension ──WS /twin──► TwinBridge
//	                               ├─ authenticates via token exchange (/pair)
//	                               ├─ stores events in AlertStore
//	                               ├─ triggers macOS notifications for high-priority events
//	                               └─ exposes health + discovery endpoints
//
// Security:
//   - Only connections from localhost (127.0.0.1 / ::1) are accepted.
//   - First WS message must be an auth message containing the session token.
//   - The session token is generated by /pair and written to the discovery file.
package twin

import (
	"context"
	"encoding/json"
	"fmt"
	"log/slog"
	"net"
	"net/http"
	"strings"
	"sync"
	"time"

	"github.com/google/uuid"
	"github.com/gorilla/websocket"
)

const (
	defaultPort       = 9800
	authTimeout       = 10 * time.Second
	readTimeout       = 60 * time.Second
	maxMessageBytes   = 64 * 1024 // 64 KiB per WS message
)

// Config holds configuration for the TwinBridge server.
type Config struct {
	// Port is the TCP port to listen on (default: 9800).
	Port int

	// MaxAlerts is the AlertStore ring-buffer capacity (default: 500).
	MaxAlerts int
}

// TwinBridge is the Twin Bridge WebSocket server.
type TwinBridge struct {
	cfg        Config
	store      *AlertStore
	meetings   *MeetingStore
	srv        *http.Server
	startAt    time.Time
	activePort int // port written to the discovery file (may differ from cfg.Port in integrated mode)

	mu    sync.RWMutex
	token string // current pairing token; empty means not paired

	db DBClient // optional — nil when Supabase is not configured
}

// DBClient is the minimal interface TwinBridge needs from the Supabase client.
type DBClient interface {
	Get(ctx context.Context, table string, query string) (json.RawMessage, error)
}

// upgrader accepts WebSocket upgrades only from localhost.
var upgrader = websocket.Upgrader{
	HandshakeTimeout: 5 * time.Second,
	ReadBufferSize:   4096,
	WriteBufferSize:  4096,
	CheckOrigin: func(r *http.Request) bool {
		return isLocalhost(r.RemoteAddr)
	},
}

// NewTwinBridge creates a TwinBridge with the given configuration.
func NewTwinBridge(cfg Config) *TwinBridge {
	if cfg.Port <= 0 {
		cfg.Port = defaultPort
	}
	return &TwinBridge{
		cfg:      cfg,
		store:    NewAlertStore(cfg.MaxAlerts),
		meetings: NewMeetingStore(),
	}
}

// SetDB wires the Supabase DB client so the bridge can serve /twin/domains.
func (tb *TwinBridge) SetDB(client DBClient) {
	tb.db = client
}

// Meetings returns the MeetingStore so MCP tools can query meeting sessions.
func (tb *TwinBridge) Meetings() *MeetingStore {
	return tb.meetings
}

// Store returns the underlying AlertStore so MCP tools can read alerts
// without going through the HTTP layer.
func (tb *TwinBridge) Store() *AlertStore {
	return tb.store
}

// Token returns the current pairing token. An empty string means the bridge
// has not been paired yet.
func (tb *TwinBridge) Token() string {
	tb.mu.RLock()
	defer tb.mu.RUnlock()
	return tb.token
}

// Init generates the initial pairing token, records the start time, and
// writes the discovery file. It must be called once before the bridge handles
// any requests — either by Start (standalone mode) or by the caller that uses
// RegisterRoutes (integrated mode).
//
// port is the port written to the discovery file so external tools can locate
// the bridge. In integrated mode this should be the main server port.
func (tb *TwinBridge) Init(port int) error {
	tok, err := generateToken()
	if err != nil {
		return fmt.Errorf("twin bridge: generate initial token: %w", err)
	}

	tb.mu.Lock()
	tb.token = tok
	tb.startAt = time.Now()
	tb.activePort = port
	tb.mu.Unlock()

	if err := writeDiscoveryFile(port, tok); err != nil {
		// Non-fatal — bridge can still operate without the discovery file.
		slog.Warn("twin bridge: failed to write discovery file", "error", err)
	}

	return nil
}

// RegisterRoutes registers all twin bridge HTTP handlers on an external mux.
// This allows the bridge to share the main server's port instead of running on
// its own listener. The caller is responsible for calling Init first.
//
// Routes registered:
//
//	GET /twin          — WebSocket endpoint
//	GET /twin/health   — Health check
//	GET /twin/pair     — Token pairing
func (tb *TwinBridge) RegisterRoutes(mux *http.ServeMux) {
	mux.HandleFunc("GET /twin/health", tb.HandleHealth)
	mux.HandleFunc("GET /twin/pair", tb.HandlePair)
	mux.HandleFunc("GET /twin", tb.HandleWS)
	mux.HandleFunc("POST /twin/push", tb.HandlePush)
	mux.HandleFunc("GET /twin/domains", tb.HandleDomains)
}

// HandleDomains returns the list of twin domains from the database.
// No authentication required — the extension reads this on startup.
//
//	GET /twin/domains
func (tb *TwinBridge) HandleDomains(w http.ResponseWriter, r *http.Request) {
	w.Header().Set("Content-Type", "application/json")
	w.Header().Set("Access-Control-Allow-Origin", "*")

	if tb.db == nil {
		// DB not configured — return empty list so extension falls back to defaults
		w.Write([]byte(`[]`))
		return
	}

	data, err := tb.db.Get(r.Context(), "twin_domains",
		"enabled=eq.true&order=sort_order.asc&select=slug,name,host,origin,icon_url,category,reader_script,monitor_script,monitor_url,monitor_interval_min,chat_click_script,chat_read_script,chat_open_script,chat_send_script,contacts_script")
	if err != nil {
		slog.Warn("twin/domains: db error", "error", err)
		w.WriteHeader(http.StatusInternalServerError)
		w.Write([]byte(`{"error":"db error"}`))
		return
	}

	w.Write(data)
}

// HandlePush receives browser events pushed by the Rust Desktop app and stores
// them in the AlertStore. This is the Go ↔ Rust integration point.
//
//   POST /twin/push
//   Content-Type: application/json
//   { "source": "slack", "event_type": "mention", "data": {...}, "timestamp": 1234567890, "origin": "desktop-bridge" }
func (tb *TwinBridge) HandlePush(w http.ResponseWriter, r *http.Request) {
	// Only accept from localhost
	if !isLocalhost(r.RemoteAddr) {
		http.Error(w, `{"error":"forbidden"}`, http.StatusForbidden)
		return
	}

	var payload struct {
		Source    string          `json:"source"`
		EventType string          `json:"event_type"`
		Data      json.RawMessage `json:"data"`
		Timestamp int64           `json:"timestamp"`
		Origin    string          `json:"origin"`
	}

	if err := json.NewDecoder(r.Body).Decode(&payload); err != nil {
		w.Header().Set("Content-Type", "application/json")
		w.WriteHeader(http.StatusBadRequest)
		w.Write([]byte(`{"error":"invalid json"}`))
		return
	}

	if payload.Source == "" {
		w.Header().Set("Content-Type", "application/json")
		w.WriteHeader(http.StatusBadRequest)
		w.Write([]byte(`{"error":"source required"}`))
		return
	}

	alert := Alert{
		ID:        uuid.New().String(),
		Source:    payload.Source,
		Type:      payload.EventType,
		Data:      payload.Data,
		CreatedAt: time.Now(),
		Read:      false,
	}

	tb.store.Add(alert)
	slog.Debug("twin push received", "source", payload.Source, "event_type", payload.EventType, "origin", payload.Origin)

	w.Header().Set("Content-Type", "application/json")
	w.Write([]byte(`{"ok":true}`))
}

// Start starts the bridge as a standalone HTTP server and writes the discovery
// file. It blocks until the server is shut down. This is kept for backward
// compatibility; prefer RegisterRoutes for integrated deployments.
func (tb *TwinBridge) Start() error {
	if err := tb.Init(tb.cfg.Port); err != nil {
		return err
	}

	mux := http.NewServeMux()
	mux.HandleFunc("GET /health", tb.HandleHealth)
	mux.HandleFunc("GET /pair", tb.HandlePair)
	mux.HandleFunc("GET /twin", tb.HandleWS)

	tb.srv = &http.Server{
		Addr:              fmt.Sprintf("127.0.0.1:%d", tb.cfg.Port),
		Handler:           localhostOnly(mux),
		ReadHeaderTimeout: 5 * time.Second,
		IdleTimeout:       60 * time.Second,
	}

	slog.Info("twin bridge starting", "addr", tb.srv.Addr)

	if err := tb.srv.ListenAndServe(); err != nil && err != http.ErrServerClosed {
		return fmt.Errorf("twin bridge: listen: %w", err)
	}
	return nil
}

// Shutdown gracefully stops the server and cleans up the discovery file.
func (tb *TwinBridge) Shutdown(ctx context.Context) error {
	defer removeDiscoveryFile()
	if tb.srv != nil {
		return tb.srv.Shutdown(ctx)
	}
	return nil
}

