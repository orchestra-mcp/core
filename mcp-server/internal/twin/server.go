// showPairingDialog presents a macOS approval dialog asking the user to allow
// the Chrome extension to connect. Returns true if the user clicks "Allow".
// On systems without osascript the function returns true automatically.
func showPairingDialog() bool {
	script := `button returned of (display dialog "A Chrome extension wants to connect to Orchestra Desktop.\n\nAllow access?" buttons {"Deny", "Allow"} default button "Allow" with title "Orchestra Twin Bridge")`
	out, err := runOsascript(script)
	if err != nil {
		// osascript unavailable (non-macOS / headless) — auto-approve.
		slog.Info("twin bridge: osascript unavailable — auto-approving pair")
		return true
	}
	return strings.TrimSpace(out) == "Allow"
}

