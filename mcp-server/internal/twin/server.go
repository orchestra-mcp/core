// localhostOnly is an HTTP middleware that rejects non-localhost connections.
func localhostOnly(next http.Handler) http.Handler {
	return http.HandlerFunc(func(w http.ResponseWriter, r *http.Request) {
		if !isLocalhost(r.RemoteAddr) {
			http.Error(w, "forbidden", http.StatusForbidden)
			return
		}
		next.ServeHTTP(w, r)
	})
}

// isLocalhost returns true when addr is a loopback address (IPv4 or IPv6).
func isLocalhost(addr string) bool {
	host, _, err := net.SplitHostPort(addr)
	if err != nil {
		host = addr
	}
	ip := net.ParseIP(host)
	if ip == nil {
		return false
	}
	return ip.IsLoopback()
}

// writeWSError sends a JSON error message over the WebSocket connection.
func writeWSError(conn *websocket.Conn, msg string) {
	writeWSJSON(conn, map[string]string{"type": "error", "message": msg})
}

// writeWSJSON serialises v to JSON and sends it as a text WebSocket message.
func writeWSJSON(conn *websocket.Conn, v interface{}) {
	data, err := json.Marshal(v)
	if err != nil {
		slog.Warn("twin bridge: marshal ws message failed", "error", err)
		return
	}
	if err := conn.WriteMessage(websocket.TextMessage, data); err != nil {
		slog.Warn("twin bridge: write ws message failed", "error", err)
	}
}
